def fmt_js_string(s: str) -> str:
    """Format a Python string as a JS single-quoted string."""
    return "'" + s.replace("\\", "\\\\").replace("'", "\\'") + "'"


def build_block(trans: dict) -> str:
    """Build the searchDn + searchDs block for a language."""
    dn_entries = []
    ds_entries = []
    for sk, _ in ITEMS:
        if sk not in trans:
            raise KeyError(f"Missing translation for {sk}")
        name, desc = trans[sk]
        dn_entries.append(f"{sk}:{fmt_js_string(name)}")
        ds_entries.append(f"{sk}:{fmt_js_string(desc)}")
    dn_block = "    searchDn: {\n      " + ", ".join(dn_entries) + "\n    },"
    ds_block = "    searchDs: {\n      " + ", ".join(ds_entries) + "\n    }"
    return dn_block + "\n" + ds_block


def patch_file(path: Path, block: str) -> None:
    text = path.read_text(encoding="utf-8")
    # Find the closing `},\n};` pattern at the end of the file
    # which closes searchKw + REGIONS.xx
    # Replace with `},\n<block>\n};`
    anchor = "    },\n};"
    if anchor not in text:
        # Try CRLF
        anchor = "    },\r\n};"
        if anchor not in text:
            raise RuntimeError(f"Anchor not found in {path}")
    # Replace only the LAST occurrence to be safe
    idx = text.rfind(anchor)
    new_text = text[:idx] + "    },\n" + block + "\n};" + text[idx + len(anchor):]
    path.write_text(new_text, encoding="utf-8")


def main():
    for lang_code, trans in T.items():
        path = LANG_DIR / f"{lang_code}.js"
        if not path.exists():
            print(f"SKIP {lang_code} (not found)")
            continue
        block = build_block(trans)
        patch_file(path, block)
        print(f"OK   {lang_code}.js: {len(trans)} items added")


if __name__ == "__main__":
    main()
